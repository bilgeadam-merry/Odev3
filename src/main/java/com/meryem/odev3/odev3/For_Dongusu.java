package com.meryem.odev3.odev3;

public class For_Dongusu {
    public static void main(String[] args) {
        /**
         * for döngüsü, belirli bir işlemi belirli bir koşul altında tekrarlamak için kullanılan
         * yapılardan biridir. Genellikle bir dizi veya koleksiyon üzerinde işlem yapmak veya belirli
         * bir aralıktaki değerleri kullanarak tekrarlı işlemler yapmak için kullanılır.
         *
         * şu şekilde yazılır
         *
         *   for (başlangıç_durumu; koşul; artış veya azalış) {
         *      Döngü içinde yapılacak işlemler
         *   }
         *
         *   NOT: Eğer süslü parantezler konulmazsa kendinden sonraki ilk satırı çalıştırıp
         *   yapılcaka işlemleri tamamlar. devamını yapmaz.
         */


         //Ornek
        for (int i = 0; i < 5; i++) {
            System.out.println("Değer: " + i);
        }
    }


}
